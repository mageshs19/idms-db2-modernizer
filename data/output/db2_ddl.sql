CREATE TABLE DENTAL_CLAIM
(
    CLAIM_DATE VARCHAR(255),
    SERVICE_DATE VARCHAR(255),
    PATIENT_FIRST_NAME VARCHAR(10),
    PATIENT_LAST_NAME VARCHAR(15),
    PATIENT_BIRTH_YEAR SMALLINT,
    PATIENT_BIRTH_MONTH SMALLINT,
    PATIENT_BIRTH_DAY SMALLINT,
    RELATION_TO_EMPLOYEE VARCHAR(10),
    DENTIST_FIRST_NAME VARCHAR(10),
    DENTIST_LAST_NAME VARCHAR(15),
    DENTIST_STREET VARCHAR(20),
    DENTIST_CITY VARCHAR(15),
    DENTIST_STATE VARCHAR(2),
    DENTIST_ZIP_FIRST_FIVE VARCHAR(5),
    DENTIST_ZIP_LAST_FOUR VARCHAR(4),
    DENTIST_LICENSE_NUMBER VARCHAR(6),
    TOOTH_NUMBER SMALLINT,
    PROCEDURE_CODE VARCHAR(4),
    DESCRIPTION_OF_SERVICE VARCHAR(60)
);

CREATE TABLE DEPARTMENT
(
    DEPT_ID SMALLINT NOT NULL,
    DEPT_NAME VARCHAR(45),
    DEPT_HEAD_ID SMALLINT,
    PRIMARY KEY (DEPT_ID)
);

CREATE TABLE EMPLOYEE
(
    START_DATE VARCHAR(255),
    BIRTH_DATE VARCHAR(255),
    TERMINATION_DATE VARCHAR(255),
    EMP_ID SMALLINT NOT NULL,
    EMP_FIRST_NAME VARCHAR(10),
    EMP_LAST_NAME VARCHAR(15),
    EMP_STREET VARCHAR(20),
    EMP_CITY VARCHAR(15),
    EMP_STATE VARCHAR(2),
    EMP_ZIP_FIRST_FIVE VARCHAR(5),
    EMP_ZIP_LAST_FOUR VARCHAR(4),
    EMP_PHONE VARCHAR(10),
    STATUS VARCHAR(2),
    SS_NUMBER VARCHAR(9),
    PRIMARY KEY (EMP_ID),
    FOREIGN KEY (OFFICE_CODE) REFERENCES OFFICE(OFFICE_CODE),
    FOREIGN KEY (DEPT_ID) REFERENCES DEPARTMENT(DEPT_ID)
);

CREATE TABLE EMPOSITION
(
    START_DATE VARCHAR(255),
    FINISH_DATE VARCHAR(255),
    SALARY_GRADE SMALLINT,
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID),
    FOREIGN KEY (JOB_ID) REFERENCES JOB(JOB_ID)
);

CREATE TABLE EXPERTISE
(
    EXPERTISE_YEAR SMALLINT,
    EXPERTISE_MONTH SMALLINT,
    EXPERTISE_DAY SMALLINT,
    FOREIGN KEY (SKILL_ID) REFERENCES SKILL(SKILL_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)
);

CREATE TABLE HOSPITAL_CLAIM
(
    CLAIM_DATE VARCHAR(255),
    ADMIT_DATE VARCHAR(255),
    DISCHARGE_DATE VARCHAR(255),
    PATIENT_FIRST_NAME VARCHAR(10),
    PATIENT_LAST_NAME VARCHAR(15),
    PATIENT_BIRTH_YEAR SMALLINT,
    PATIENT_BIRTH_MONTH SMALLINT,
    PATIENT_BIRTH_DAY SMALLINT,
    RELATION_TO_EMPLOYEE VARCHAR(10),
    HOSPITAL_NAME VARCHAR(25),
    HOSP_STREET VARCHAR(20),
    HOSP_CITY VARCHAR(15),
    HOSP_STATE VARCHAR(2),
    HOSP_ZIP_FIRST_FIVE VARCHAR(5),
    HOSP_ZIP_LAST_FOUR VARCHAR(4)
);

CREATE TABLE INSURANCE_PLAN
(
    INS_PLAN_CODE VARCHAR(3) NOT NULL,
    INS_CO_NAME VARCHAR(45),
    INS_CO_STREET VARCHAR(20),
    INS_CO_CITY VARCHAR(15),
    INS_CO_STATE VARCHAR(2),
    INS_CO_ZIP_FIRST_FIVE VARCHAR(5),
    INS_CO_ZIP_LAST_FOUR VARCHAR(4),
    INS_CO_PHONE VARCHAR(10),
    GROUP_NUMBER INTEGER,
    PLAN_DESCRIPTION VARCHAR(45),
    PRIMARY KEY (INS_PLAN_CODE)
);

CREATE TABLE JOB
(
    JOB_ID SMALLINT NOT NULL,
    TITLE VARCHAR(20),
    NUMBER_OF_POSITIONS SMALLINT,
    NUMBER_OPEN SMALLINT,
    PRIMARY KEY (JOB_ID)
);

CREATE TABLE NON_HOSP_CLAIM
(
    CLAIM_DATE VARCHAR(255),
    SERVICE_DATE VARCHAR(255),
    PATIENT_FIRST_NAME VARCHAR(10),
    PATIENT_LAST_NAME VARCHAR(15),
    PATIENT_BIRTH_YEAR SMALLINT,
    PATIENT_BIRTH_MONTH SMALLINT,
    PATIENT_BIRTH_DAY SMALLINT,
    RELATION_TO_EMPLOYEE VARCHAR(10),
    PHYSICIAN_FIRST_NAME VARCHAR(10),
    PHYSICIAN_LAST_NAME VARCHAR(15),
    PHYSICIAN_STREET VARCHAR(20),
    PHYSICIAN_CITY VARCHAR(15),
    PHYSICIAN_STATE VARCHAR(2),
    PHYSICIAN_ZIP_FIRST_FIVE VARCHAR(5),
    PHYSICIAN_ZIP_LAST_FOUR VARCHAR(4),
    PHYSICIAN_ID INTEGER,
    PROCEDURE_CODE VARCHAR(4),
    DESCRIPTION_OF_SERVICE VARCHAR(60)
);

CREATE TABLE OFFICE
(
    OFFICE_CODE VARCHAR(3) NOT NULL,
    OFFICE_STREET VARCHAR(20),
    OFFICE_CITY VARCHAR(15),
    OFFICE_STATE VARCHAR(2),
    OFFICE_ZIP_FIRST_FIVE VARCHAR(5),
    OFFICE_ZIP_LAST_FOUR VARCHAR(4),
    OFFICE_AREA_CODE VARCHAR(3),
    SPEED_DIAL VARCHAR(3),
    PRIMARY KEY (OFFICE_CODE)
);

CREATE TABLE SKILL
(
    SKILL_ID SMALLINT NOT NULL,
    SKILL_NAME VARCHAR(12),
    SKILL_DESCRIPTION VARCHAR(60),
    PRIMARY KEY (SKILL_ID)
);

CREATE TABLE STRUCTURE
(
    STRUCTURE_CODE VARCHAR(2),
    STRUCTURE_YEAR SMALLINT,
    STRUCTURE_MONTH SMALLINT,
    STRUCTURE_DAY SMALLINT,
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)
);
